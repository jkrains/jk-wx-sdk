package com.jk.wx.sdk.qw.bean.tag;

public class TagUser {

  private String userid;
  private String name;

  public TagUser() {
  }

  public String getUserid() {
    return userid;
  }

  public TagUser setUserid(String userid) {
    this.userid = userid;
    return this;
  }

  public String getName() {
    return name;
  }

  public TagUser setName(String name) {
    this.name = name;
    return this;
  }
}
