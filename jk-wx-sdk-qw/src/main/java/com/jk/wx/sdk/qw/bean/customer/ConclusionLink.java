package com.jk.wx.sdk.qw.bean.customer;

public class ConclusionLink {

  private String title;
  private String picurl;
  private String desc;
  private String url;

  public ConclusionLink() {
  }

  public String getTitle() {
    return title;
  }

  public ConclusionLink setTitle(String title) {
    this.title = title;
    return this;
  }

  public String getPicurl() {
    return picurl;
  }

  public ConclusionLink setPicurl(String picurl) {
    this.picurl = picurl;
    return this;
  }

  public String getDesc() {
    return desc;
  }

  public ConclusionLink setDesc(String desc) {
    this.desc = desc;
    return this;
  }

  public String getUrl() {
    return url;
  }

  public ConclusionLink setUrl(String url) {
    this.url = url;
    return this;
  }
}
